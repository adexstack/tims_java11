package FirstSteps;

// BigDecimal is recommended when floating point precisions are required.
public class Challenge1 {
    public static void main(String[] args) {
        double noOfPounds = 200d;
        double  convertedToKg = noOfPounds * 0.453592;
        System.out.println(convertedToKg);

        // Unicode characters    https://unicode-table.com/en/
        char myChar = 'D';
        char unicode = '\u00B5';
        System.out.println(myChar);
        System.out.println(unicode);

    }
}
